/**
 * Load a single Google Font dynamically.
 * Skips if already loaded to avoid duplicate requests.
 */
export function loadGoogleFont(fontName) {
    const encoded = fontName.replace(/\s+/g, '+');
    const linkId = 'gf-' + encoded;
    const existing = document.getElementById(linkId);

    return new Promise((resolve) => {
        const finalize = () => resolve();
        if (existing) {
            if (existing.dataset.loaded === 'true') return finalize();
            const handler = () => {
                existing.removeEventListener('load', handler);
                existing.removeEventListener('error', handler);
                finalize();
            };
            existing.addEventListener('load', handler);
            existing.addEventListener('error', handler);
            setTimeout(finalize, 1500); // Fail-safe
            return;
        }

        const link = document.createElement('link');
        link.id = linkId;
        link.rel = 'stylesheet';
        // Use display=block to prevent swap-flicker and ensure the browser prioritizes the web font
        // NOT specifying weights allows Google to return all available weights for the family
        link.href = `https://fonts.googleapis.com/css2?family=${encoded}&display=block`;
        link.onload = () => {
            link.dataset.loaded = 'true';
            finalize();
        };
        link.onerror = () => {
            link.dataset.loaded = 'error';
            finalize();
        };
        document.head.appendChild(link);
        setTimeout(finalize, 2500); // Hard safety fallback
    });
}

/**
 * Batch load multiple fonts at once for dropdown previews.
 * Splits into chunks to avoid excessively long URLs.
 */
export function batchLoadFonts(fontNames) {
    const CHUNK_SIZE = 15;
    for (let i = 0; i < fontNames.length; i += CHUNK_SIZE) {
        const chunk = fontNames.slice(i, i + CHUNK_SIZE);
        const batchList = chunk.map(f => f.replace(/\s+/g, '+')).join('&family=');
        if (batchList.length > 0) {
            const encoded = `family=${batchList}`;
            if (!document.querySelector(`link[href*="${encoded}"]`)) {
                const link = document.createElement('link');
                link.rel = 'stylesheet';
                link.href = `https://fonts.googleapis.com/css2?family=${batchList}&display=swap`;
                document.head.appendChild(link);
            }
        }
    }
}
